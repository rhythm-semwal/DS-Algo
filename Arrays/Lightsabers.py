class Solution:
    # @param A : list of integers
    # @param B : list of integers
    # @return an integer
    def solve(self, A, B):
        hash_map = dict()
        A.sort()
        for i in range(len(A)):
            if A[i] not in hash_map:
                hash_map[A[i]] = 1
            else:
                hash_map[A[i]] += 1

        print(hash_map)

        if len(hash_map) < len(B):
            return -1
        count = 0
        m = 1
        for i in range(len(B)):
            if hash_map[m] == B[i]:
                m += 1
                continue
            elif hash_map[m] < B[i]:
                count += B[i] - hash_map[m]
            else:
                count += hash_map[m] - B[i]
            m += 1
        return count


